    // metodo de pesquisa no banco
    private void consultar() {
        String sql = " select*from usuario where id =?";
        try {
            // envia a consulta para BD
            pst = con.prepareStatement(sql);
            // seta o valor de id para o primeiro indice do comando
            pst.setString(1, txtID.getText());
            // recebe a consulta com executeQuery que sera usado 
            // somente para select 
            rs = pst.executeQuery();
            if (rs.next()) {
                txtNome.setText(rs.getString(2));
                txtLogin.setText(rs.getString(3));
                txtSenha.setText(rs.getString(4));
                cbUsuPerfil.setSelectedItem(rs.getString(5));
            }
        } catch (Exception e) {
        }

    }

    private void inserir() {
        String sql = "insert into usuario (nome,login,senha, perfil) values (?,?,?,?);";
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtLogin.getText());
            pst.setString(3, txtSenha.getText());
            pst.setString(4, cbUsuPerfil.getSelectedItem().toString());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Inserido com sucesso");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void atualizar() {
        String sql = "update usuario set nome = ?, login = ?,senha,perfil = ? where id = ?";
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtLogin.getText());
            pst.setString(3, txtSenha.getText());
            pst.setString(4, cbUsuPerfil.getSelectedItem().toString());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Atualizado com sucesso");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void excluir() {
        String sql = "delete from usuario where id = ?;";
        
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, txtID.getText());
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Deletado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

